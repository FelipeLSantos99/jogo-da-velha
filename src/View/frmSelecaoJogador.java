    public void mostrarJogadoresComboBox() {

        try {
            JogadorDAO objJogadorDAO = new JogadorDAO();
            ResultSet rs = objJogadorDAO.listarJogadorComboBox();

            while (rs.next()) {
                cbxJogador1.addItem(rs.getString(1));
                cbxJogador2.addItem(rs.getString(1));
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "mostrarJogadoresComboBoxVIEW: " + erro);
        }

    }

    public void enviarJogador() {
        String jogador1, jogador2;
        jogador1 = cbxJogador1.getSelectedItem().toString();
        jogador2 = cbxJogador2.getSelectedItem().toString();
        frmJogo jogadores = new frmJogo();
        jogadores.recebeJogador(jogador1, jogador2);
        jogadores.setVisible(true);

        this.dispose();

    }

